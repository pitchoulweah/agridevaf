    public function getlastCommande($value): ?Commande
    {
        return $this->createQueryBuilder('c')
            ->andWhere('c.client = :val AND c.envoye = false')
            ->setParameter('val', $value)
            ->getQuery()
            ->getOneOrNullResult()
        ;
    }
